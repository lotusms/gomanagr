/**
 * Default palette to use
 */
const defaultPalette = 'palette1';

/**
 * Get a specific palette by ID
 * @param {string} paletteId - The palette ID (e.g., 'palette1')
 * @returns {Object} The palette object
 */
function getPalette(paletteId = defaultPalette) {
  return palettes[paletteId] || palettes[defaultPalette];
}

/**
 * Get all available palettes
 * @returns {Object} All palettes
 */
function getAllPalettes() {
  return palettes;
}

/**
 * Get palette colors formatted for Tailwind config
 * @param {string} paletteId - The palette ID
 * @returns {Object} Colors object compatible with Tailwind config
 */
function getPaletteForTailwind(paletteId = defaultPalette) {
  const palette = getPalette(paletteId);
  return palette.colors;
}

